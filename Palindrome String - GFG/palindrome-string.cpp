class Solution{
public:	
	
	
	int isPalindrome(string s)
	{
	    // Your code goes here
	    int st=0;
	    int e=s.size()-1;
	    
	    while(st<e){
	        if(s[st]!=s[e]){
	            return false;
	        }
	        else{
	            st++;
	            e--;
	        }
	    }
	    return true;
	}

};

